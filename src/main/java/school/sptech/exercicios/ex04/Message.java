package school.sptech.exercicios.ex04;

public class Message {
    
    // attributes
    private String message;

    // constructor
    public Message(String message) {
        this.message = message;
    }

    // getters and setters
    public String getMessage() {
        return message;
    }
    public void setMessage(String message) {
        this.message = message;
    }
}
